        private void IsValidCommandText(string commandText)
        {
            if (commandText.IsNullOrEmpty()) throw new Exception("Query is blank.");
        }

        private void PrepareCommand(IDbCommand command, string cmdText)
        {
            command.CommandType = CommandType.Text;
            command.CommandText = cmdText;
        }

        protected void PrepareCommandParameter(IDbCommand command, object[] parameters)
        {
            if (parameters.IsNull() || parameters.Length.IsZero()) return;
            SetParameters(command, parameters);
        }

        protected void SetParameters(IDbCommand command, object[] parameterValues)
        {
            var parameters = new IDbDataParameter[parameterValues.Length];

            for (var i = 0; i < parameterValues.Length; i++)
            {
                var value = parameterValues[i];
                parameters[i] = command.CreateParameter();
                parameters[i].DbType = MappedDbType.GetDbType(value.GetType());
                parameters[i].ParameterName = i.ToString();
                parameters[i].Value = value;
                command.Parameters.Add(parameters[i]);
            }
        }

