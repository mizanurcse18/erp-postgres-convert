        protected override void OnModelCreating(ModelBuilder modelBuilder)
        {
            base.OnModelCreating(modelBuilder);
            
            // Apply snake_case naming convention for PostgreSQL compatibility
            foreach (var entity in modelBuilder.Model.GetEntityTypes())
            {
                // Convert table names to snake_case
                var tableName = entity.GetTableName();
                if (!string.IsNullOrEmpty(tableName))
                {
                    entity.SetTableName(ToSnakeCase(tableName));
                }

                // Convert column names to snake_case
                foreach (var property in entity.GetProperties())
                {
                    var columnName = property.GetColumnName();
                    if (!string.IsNullOrEmpty(columnName))
                    {
                        property.SetColumnName(ToSnakeCase(columnName));
                    }
                }

                // Convert key names to snake_case
                var key = entity.FindPrimaryKey();
                if (key != null)
                {
                    key.SetName(ToSnakeCase(key.GetName()));
                }

                // Convert foreign key constraint names to snake_case
                foreach (var foreignKey in entity.GetForeignKeys())
                {
                    foreignKey.SetConstraintName(ToSnakeCase(foreignKey.GetConstraintName()));
                }

                // Note: Index naming conversion skipped for EF Core 3.1 compatibility
                // Indexes will use default names generated by Npgsql
            }
        }

        private static string ToSnakeCase(string name)
        {
            if (string.IsNullOrEmpty(name))
                return name;

            // Insert an underscore between a letter and that letter + 1 lowercase letter / number
            // e.g., "CompanyTerms" -> "Company_Terms"
            var s1 = Regex.Replace(name, @"([a-z])([A-Z])", "$1_$2");
            
            // Insert an underscore between any number and a letter
            // e.g., "UserID123" -> "UserID_123"
            var s2 = Regex.Replace(s1, @"([0-9])([a-zA-Z])", "$1_$2");
            
            // Convert to lowercase
            return s2.ToLower();
        }

