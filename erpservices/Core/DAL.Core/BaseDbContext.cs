        public bool BulkInsertFromDataTableToMSSQL(string tableName, DataTable dataTable, bool isTruncateTable, string[] DbColumnName, string[] InputColumnName)
        {

            if (DbColumnName.Length != InputColumnName.Length)
                return false;

            using (SqlConnection connection = new SqlConnection(Database.GetDbConnection().ConnectionString))
            {
                connection.Open();
                if (isTruncateTable)
                {
                    using (SqlCommand command = connection.CreateCommand())
                    {
                        command.CommandText = $"truncate table {tableName}";
                        command.Parameters.Clear();
                        command.ExecuteNonQuery();
                    }
                }

                using (SqlBulkCopy bulkCopy = new SqlBulkCopy(connection))
                {
                    bulkCopy.DestinationTableName = tableName;

                    try
                    {
                        for (int i = 0; i < DbColumnName.Length; i++)
                        {
                            bulkCopy.ColumnMappings.Add(InputColumnName[i], DbColumnName[i]);
                        }

                        bulkCopy.WriteToServer(dataTable);
                        Console.WriteLine("Bulk insert completed.");
                        return true;
                    }
                    catch (Exception ex)
                    {
                        Console.WriteLine("Error: " + ex.Message);
                        return false;
                    }
                }
            }
        }

        public bool BulkInsertFromDataTableToPostgreSQL(string tableName, DataTable dataTable, bool isTruncateTable, string[] dbColumnName, string[] inputColumnName)
        {
            if (dbColumnName.Length != inputColumnName.Length)
                return false;

            var connection = Database.GetDbConnection();
            var wasOpen = connection.State == System.Data.ConnectionState.Open;

            try
            {
                if (!wasOpen)
                {
                    Database.OpenConnection();
                }

                if (isTruncateTable)
                {
                    using (var command = connection.CreateCommand())
                    {
                        command.CommandText = $"TRUNCATE TABLE {tableName} RESTART IDENTITY CASCADE";
                        command.ExecuteNonQuery();
                    }
                }

                using (var transaction = connection.BeginTransaction())
                {
                    foreach (DataRow row in dataTable.Rows)
                    {
                        using (var command = connection.CreateCommand())
                        {
                            command.Transaction = transaction;
                            var columnNames = new List<string>();
                            var parameterNames = new List<string>();

                            for (int i = 0; i < dbColumnName.Length; i++)
                            {
                                columnNames.Add(dbColumnName[i]);
                                parameterNames.Add($":p{i}");
                                var parameter = command.CreateParameter();
                                parameter.ParameterName = $"p{i}";
                                parameter.Value = row[inputColumnName[i]] ?? DBNull.Value;
                                command.Parameters.Add(parameter);
                            }

                            command.CommandText = $"INSERT INTO {tableName} ({string.Join(", ", columnNames)}) VALUES ({string.Join(", ", parameterNames)})";
                            command.ExecuteNonQuery();
                        }
                    }

                    transaction.Commit();
                }

                Console.WriteLine("Bulk insert completed.");
                return true;
            }
            catch (Exception ex)
            {
                Console.WriteLine("Error: " + ex.Message);
                return false;
            }
            finally
            {
                if (!wasOpen)
                {
                    Database.CloseConnection();
                }
            }
        }

