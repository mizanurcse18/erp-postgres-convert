        public GridModel LoadGridModel(GridParameter parameters, string commandText)
        {
            var gridModel = new GridModel();
            var list = new List<Dictionary<string, object>>();
            var cols = new List<string>();

            var totalRows = 0;

            commandText = MakeSearchQuery(parameters, commandText);


            using (var command = Database.GetDbConnection().CreateCommand())
            {
                command.CommandText = commandText;
                Database.OpenConnection();

                using (var reader = command.ExecuteReader())
                {
                    for (var i = 0; i < reader.FieldCount; i++)
                    {
                        var name = reader.GetName(i);
                        if (//name != "AutoGenRowNum" && 
                            name != "TotalRows")
                        {
                            cols.Add(name);
                        }
                    }

                    //First Row
                    if (reader.Read())
                    {
                        if (parameters.ServerPagination) totalRows = Convert.ToInt32(reader["TotalRows"]);
                        list.Add(GetFields(cols, reader));
                    }
                    while (reader.Read())
                    {
                        list.Add(GetFields(cols, reader));
                    }

                }

                Database.CloseConnection();
            }

            gridModel.Rows = list;
            gridModel.Total = parameters.ServerPagination ? totalRows : list.Count;

            return gridModel;
        }
