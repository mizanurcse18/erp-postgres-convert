        private static readonly string[] listOfEscapeCharacters = new[] { "'" };

        internal string MakeSearchQuery(GridParameter parameters, string sql)
        {
            int offset = parameters.Offset * parameters.Limit;
            string[] searchByArray = Array.Empty<string>(),
                searchArray = Array.Empty<string>(),
                searchTypeArray = Array.Empty<string>();

            if (parameters.SearchBy.IsNotNullOrEmpty())
            {
                searchByArray = parameters.SearchBy.Split('$');
                if (parameters.Search.IsNull())
                {
                    for (int i = 0; i < searchByArray.Length; i++)
                    {
                        parameters.Search += '$';
                    }
                }

                searchArray = parameters.Search.Split('$');
                searchTypeArray = parameters.SearchType.IsNotNullOrEmpty() ? parameters.SearchType.Split('$') : string.Empty.Split('$');
            }

            var filter = string.Empty;
            if (parameters.SearchBy.IsNotNullOrEmpty() && parameters.Search.IsNotNullOrEmpty())
            {
                for (int i = 0; i < searchByArray.Length; i++)
                {
                    listOfEscapeCharacters.ToList().ForEach(character =>
                    {
                        searchArray[i] = searchArray[i].Replace(character, $"'{character}");
                    });

                    if (parameters.SearchType.IsNotNullOrEmpty())
                    {
                        if (searchTypeArray[i].Equals("date"))
                        {
                            string currentCulture = Util.SysDateFormat;
                            int currentCultureCode = 103;

                            switch (currentCulture)
                            {
                                case "dd/MM/yyyy":
                                    currentCultureCode = 103;
                                    break;
                                case "MM/dd/yyyy":
                                    currentCultureCode = 101;
                                    break;
                                case "yyyy/MM/dd":
                                    currentCultureCode = 111;
                                    break;
                                case "dd-MM-yyyy":
                                    currentCultureCode = 105;
                                    break;
                            }

                            filter += string.Format("CONVERT(VARCHAR, {0}, {2}) LIKE '%{1}%'", searchByArray[i], searchArray[i], currentCultureCode);
                        }
                        else
                        {
                            filter += $"{searchByArray[i]} LIKE '%{searchArray[i]}%'";
                        }
                    }
                    else
                    {
                        filter += $"{searchByArray[i]} LIKE '%{searchArray[i]}%'";
                    }

                    if (i + 1 < searchByArray.Length)
                    {
                        filter += " AND ";
                    }
                }

                filter = "WHERE " + filter;
            }

            var order = string.Empty;
            if (parameters.SortName.IsNotNullOrEmpty() && parameters.SortOrder.IsNotNullOrEmpty())
            {
                if (parameters.SortOrder.TrimStart('$').IsNotNullOrEmpty())
                {
                    var sortNameArray = parameters.SortName.Split('$');
                    var sortOrderArray = parameters.SortOrder.Split('$');

                    for (int i = 0; i < sortNameArray.Length; i++)
                    {
                        if (sortOrderArray[i].IsNullOrEmpty()) continue;
                        order += $"{sortNameArray[i]} {sortOrderArray[i]}" + ", ";
                    }

                    order = "ORDER BY " + order.TrimEnd(' ').TrimEnd(',');
                }
            }

            if (order.IsNullOrEmpty() && parameters.Sort.IsNotNullOrEmpty())
            {
                order = $"ORDER BY {parameters.Sort} {parameters.Order}";
            }

            if (parameters.ServerPagination)
            {
                return $@"WITH TAB
                        AS 
                        ( 
	                        SELECT ROW_NUMBER() OVER ({order} {string.Empty}) AS AutoGenRowNum, COUNT(*) OVER () AS TotalRows, TEMP.*
	                        FROM
	                        (

		                        {sql}

	                        ) TEMP

                            {filter}
                        )

                        SELECT * FROM TAB Where AutoGenRowNum > {offset} AND AutoGenRowNum <= {offset + parameters.Limit}";
            }

            return $@"SELECT * FROM ({sql}) AS TEMP {filter} {order}";
        }
